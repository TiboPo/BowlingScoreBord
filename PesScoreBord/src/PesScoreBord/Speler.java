public class Speler {
    private String naam;
    private Score score;
    private int punten;

    public Speler(String naam) {
        this.naam = naam;
        this.punten = score.getScore();
    }
    
    public String getNaam(){
        return naam;
    }
    public int getScore(){
        return punten;
    }
   
    
}
